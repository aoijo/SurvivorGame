package Entity;

public class Time {
    private int Year;
    private int Month;
    private int Day;
    private int Hour;
    private int Minute;

    public Time() {
    }

    public int getYear() {
        return Year;
    }

    public void setYear(int Year) {
        this.Year = Year;
    }

    public int getMonth() {
        return Month;
    }
    public void setMonth(int Month) {
        this.Month = Month;
    }

    public int getDay() {
        return Day;
    }

    public void setDay(int Day) {
        this.Day = Day;
    }

    public int getHour() {
        return Hour;
    }

    public void setHour(int Hour) {
        this.Hour = Hour;
    }

    public int getMinute() {
        return Minute;
    }

    public void setMinute(int Minute) {
        this.Minute = Minute;
    }
}
